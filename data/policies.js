/** Returns unique categories across all policies */
function getPolicyCategories() {
  const cats = new Set();
  POLICIES.forEach(p => p.categories.forEach(c => cats.add(c)));
  return Array.from(cats).sort();
}

/** Returns unique statuses */
function getPolicyStatuses() {
  return ["active", "forthcoming", "historical"];
}

/** Filter policies by category, status, sector, and keyword */
function filterPolicies({ category = null, status = null, sector = null, keyword = "" } = {}) {
  return POLICIES.filter(p => {
    if (category && !p.categories.includes(category)) return false;
    if (status && p.status !== status) return false;
    if (sector && !p.sectors.includes("all") && !p.sectors.includes(sector)) return false;
    if (keyword) {
      const kw = keyword.toLowerCase();
      return (
        p.title.toLowerCase().includes(kw) ||
        p.summary.toLowerCase().includes(kw) ||
        p.keyProvisions.some(kp => kp.toLowerCase().includes(kw))
      );
    }
    return true;
  });
}
